     * @return \Illuminate\Http\Response
     * @throws \Exception
     */
    public function index()
    {
        return DataTables::of(Product::with('state'))
            ->addColumn('actions', 'admin.products.partials.actions')
            ->editColumn('value', function(Product $product) {
                return "$".number_format($product->value);
            })
            ->editColumn('tax_percentage', function(Product $product) {
                return $product->tax_percentage."%";
            })
            ->rawColumns(['actions'])
            ->toJson();
    }

